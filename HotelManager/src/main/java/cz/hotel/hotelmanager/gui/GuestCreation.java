    private void emptyTextFields() {   
        txtValName.setText("");
        txtValAddress.setText("");
        txtValPhoneNum.setText("");
    }
    
    private class AddGuest extends SwingWorker<Void, Void> {

        @Override
        protected Void doInBackground() throws Exception {
            GuestBuilder build = new GuestBuilder();
            build.name(guestData.get("name"));
            build.address(guestData.get("address"));
            build.phoneNumber(guestData.get("phone"));
            Guest guest = build.build();
            manager.createGuest(guest);
            return null;
        }
        
    }

