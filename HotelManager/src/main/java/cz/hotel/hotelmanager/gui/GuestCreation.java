    private void buttonCreateGuestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCreateGuestActionPerformed
        String name = txtValName.getText();
        String address = txtValAddress.getText();
        String phoneNum = txtValPhoneNum.getText();
        
        if (name.isEmpty() || address.isEmpty() || phoneNum.isEmpty()) {
            logger.warn("Guest creation failed: name, address, or phoneNum is empty");
            return;
        }
        
        this.setVisible(false);
        guestData = new HashMap<>();
        guestData.put("name", name);
        guestData.put("address", address);
        guestData.put("phone", phoneNum);
        
        emptyTextFields();
        new AddGuest().execute();
    }//GEN-LAST:event_buttonCreateGuestActionPerformed

